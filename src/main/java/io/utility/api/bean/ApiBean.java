package io.utility.api.bean;

public class ApiBean {

	private String apiHttps;
	private String authorization;
	private String contentType;
	private String urlToken;
	private String urlMessage;

	public String getApiHttps() {
		return apiHttps;
	}
	public void setApiHttps(String apiHttps) {
		this.apiHttps = apiHttps;
	}
	public String getAuthorization() {
		return authorization;
	}
	public void setAuthorization(String authorization) {
		this.authorization = authorization;
	}
	public String getContentType() {
		return contentType;
	}
	public void setContentType(String contentType) {
		this.contentType = contentType;
	}
	public String getUrlToken() {
		return urlToken;
	}
	public void setUrlToken(String urlToken) {
		this.urlToken = urlToken;
	}
	public String getUrlMessage() {
		return urlMessage;
	}
	public void setUrlMessage(String urlMessage) {
		this.urlMessage = urlMessage;
	}

}
